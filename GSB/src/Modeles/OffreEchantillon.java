package Modeles;

public class OffreEchantillon {
	
	private int NumOffreEchantillon;
	private int Quantite;
	private int NumMedicament;
	private int NumRapport;
	
	
	public int getNumOffreEchantillon() {
		return NumOffreEchantillon;
	}
	public void setNumOffreEchantillon(int numOffreEchantillon) {
		NumOffreEchantillon = numOffreEchantillon;
	}
	public int getQuantite() {
		return Quantite;
	}
	public void setQuantite(int quantite) {
		Quantite = quantite;
	}
	public int getNumMedicament() {
		return NumMedicament;
	}
	public void setNumMedicament(int numMedicament) {
		NumMedicament = numMedicament;
	}
	public int getNumRapport() {
		return NumRapport;
	}
	public void setNumRapport(int numRapport) {
		NumRapport = numRapport;
	}

}
